type StageReport = {
  stage: string;
  file: string;
  line?: string;
  data: Record<string, unknown>;
};

function traceParty(
  label: string,
  result: ExtractionResult,
  expected: { type: string; amountOrPct: number }
): { stages: StageReport[]; firstDivergence: string | null } {
  const stages: StageReport[] = [];
  const original = result.parties[0]!;
  const party = original;

  stages.push({
    stage: '1_raw_extraction',
    file: 'extraction-service.ts (validateExtractionResult output)',
    data: {
      participationModel: party.participationModel.value,
      fixedAmount: party.fixedAmount.value,
      revenueSharePct: party.revenueSharePct.value,
      currency: result.currency.value,
      currencyConfidence: result.currency.confidence,
    },
  });

  const form = reviewFormFromExtraction(result, 'participant_add', 'whatsapp', undefined, {
    workspaceCurrency: 'AUD',
  });
  const reviewed = form.parties[0]!;

  stages.push({
    stage: '2_review_form',
    file: 'review-form-types.ts:reviewFormFromExtraction',
    line: '54-107',
    data: {
      participationModel: reviewed.participationModel,
      fixedAmount: reviewed.fixedAmount,
      revenueSharePct: reviewed.revenueSharePct,
      formCurrency: form.currency,
      extractedCurrencyUnsupported: form.extractedCurrencyUnsupported,
    },
  });

  stages.push({
    stage: '3_mapper_input',
    file: 'extraction-mapper.ts:mapSinglePartyToParticipant (ReviewedParty arg)',
    data: {
      participationModel: reviewed.participationModel,
      fixedAmount: reviewed.fixedAmount,
      revenueSharePct: reviewed.revenueSharePct,
    },
  });

  const mapped = mapSinglePartyToParticipant(reviewed, baseDeal(), '[AI Import]', original);

  stages.push({
    stage: '4_before_persist',
    file: 'extraction-mapper.ts:mapSinglePartyToParticipant return',
    line: '195-196',
    data: {
      participationModel: mapped.participationModel,
      commissionKind: mapped.commissionKind,
      commissionValue: mapped.commissionValue,
      compensationType: mapped.compensationProfile?.compensationType ?? null,
      profilePercentage: mapped.compensationProfile?.percentage ?? null,
      profileFixedAmount: mapped.compensationProfile?.fixedAmount ?? null,
      profileConfigured: mapped.compensationProfile?.configured ?? null,
    },
  });

  const dbPayload = simulateDbPayload(mapped);
  stages.push({
    stage: '5_db_payload',
    file: 'pilot-snapshot.server.ts:participant_payload (JSON round-trip)',
    data: {
      compensationType: dbPayload.compensationProfile?.compensationType ?? null,
      profilePercentage: dbPayload.compensationProfile?.percentage ?? null,
      profileFixedAmount: dbPayload.compensationProfile?.fixedAmount ?? null,
      profileConfigured: dbPayload.compensationProfile?.configured ?? null,
    },
  });

  const reloaded = simulateReload(dbPayload);
  const hydratedEntity = hydrateParticipant(reloaded)._entity;
  const hydratedOp = hydrateOperationalParticipant(reloaded);
  const compState = deriveCompensationState(hydratedEntity);

  stages.push({
    stage: '6_after_reload',
    file: 'getPilotSnapshotForUser + repairScalarCompensationProfile',
    data: {
      compensationType: reloaded.compensationProfile?.compensationType ?? null,
      profileConfigured: reloaded.compensationProfile?.configured ?? null,
    },
  });

  stages.push({
    stage: '7_hydrated_checks',
    file: 'participant-earnings-primitives + derive-compensation-state',
    data: {
      hasPersistedCompensationTerms: hasPersistedCompensationTerms(hydratedEntity),
      deriveCompensationState: {
        configured: compState.configured,
        type: compState.type,
        earningsPrimaryCompact: compState.earningsPrimaryCompact,
        settlementBasis: compState.settlementBasis,
      },
    },
  });

  let firstDivergence: string | null = null;
  const profile = mapped.compensationProfile;
  if (expected.type === 'FIXED_FEE') {
    if (profile?.compensationType !== 'FIXED_FEE' || profile.fixedAmount !== expected.amountOrPct) {
      firstDivergence = findFirstStage(stages, '4_before_persist', profile);
    }
  } else if (expected.type === 'REVENUE_SHARE') {
    if (
      profile?.compensationType !== 'REVENUE_SHARE' ||
      profile.percentage !== expected.amountOrPct
    ) {
      firstDivergence = findFirstStage(stages, '4_before_persist', profile);
    }
  }

  if (!firstDivergence && !hasPersistedCompensationTerms(hydratedEntity)) {
    firstDivergence = '7_hydrated_checks — hasPersistedCompensationTerms false';
  }

  return { stages, firstDivergence };
}

function findFirstStage(
  stages: StageReport[],
  fromStage: string,
  profile: DemoParticipant['compensationProfile']
): string {
  for (const s of stages) {
    if (s.stage >= fromStage || s.stage === fromStage) {
      return `${s.stage} @ ${s.file} — profile=${JSON.stringify(profile ?? null)}`;
    }
  }
  return `${fromStage} — profile missing or wrong`;
}

function printReport(label: string, report: ReturnType<typeof traceParty>) {
  console.log('\n' + '='.repeat(72));
  console.log(label);
  console.log('='.repeat(72));
  for (const s of report.stages) {
    console.log(`\n[${s.stage}] ${s.file}${s.line ? `:${s.line}` : ''}`);
    console.log(JSON.stringify(s.data, null, 2));
  }
  console.log('\nFIRST DIVERGENCE:', report.firstDivergence ?? 'none (matches expected through hydration)');
}

async function main() {
  const useLive = Boolean(process.env.ANTHROPIC_API_KEY);
  if (useLive) {
    console.log('--- LIVE AI EXTRACTION ---');
    for (const key of ['islandDjs', 'coastal'] as const) {
      const c = CONVERSATIONS[key];
      const live = await extractAgreementFromText(c.text);
      const party = live.parties[0];
      console.log(`\n${c.label} (live)`);
      console.log(
        JSON.stringify(
          {
            partiesCount: live.parties.length,
            participationModel: party?.participationModel.value,
            fixedAmount: party?.fixedAmount.value,
            revenueSharePct: party?.revenueSharePct.value,
            currency: live.currency.value,
            uncertainties: live.uncertainties,
          },
          null,
          2
        )
      );
      const expected =
        key === 'islandDjs'
          ? { type: 'FIXED_FEE', amountOrPct: 2500 }
          : { type: 'REVENUE_SHARE', amountOrPct: 15 };
      printReport(c.label + ' LIVE', traceParty(c.label, live, expected));
    }
  }

  console.log('\n\n--- IDEAL EXTRACTION SHAPES ---');
  printReport(
    CONVERSATIONS.islandDjs.label + ' IDEAL',
    traceParty(
      CONVERSATIONS.islandDjs.label,
      CONVERSATIONS.islandDjs.idealExtraction(),
      { type: 'FIXED_FEE', amountOrPct: 2500 }
    )
  );
  printReport(
    CONVERSATIONS.coastal.label + ' IDEAL',
    traceParty(
      CONVERSATIONS.coastal.label,
      CONVERSATIONS.coastal.idealExtraction(),
      { type: 'REVENUE_SHARE', amountOrPct: 15 }
    )
  );

  console.log('\n\n--- FAILURE SHAPES (production-like) ---');
  printReport(
    CONVERSATIONS.islandDjs.label + ' fixedAmount absent',
    traceParty(
      CONVERSATIONS.islandDjs.label,
      CONVERSATIONS.islandDjs.failureExtractionMissingAmount(),
      { type: 'FIXED_FEE', amountOrPct: 2500 }
    )
  );
  printReport(
    CONVERSATIONS.coastal.label + ' customer_attribution label',
    traceParty(
      CONVERSATIONS.coastal.label,
      CONVERSATIONS.coastal.failureExtractionAttribution(),
      { type: 'REVENUE_SHARE', amountOrPct: 15 }
    )
  );
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
