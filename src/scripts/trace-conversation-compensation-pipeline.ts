function simulateDbPayload(participant: DemoParticipant): DemoParticipant {
  const data = JSON.parse(JSON.stringify(participant)) as DemoParticipant;
  return data;
}

function simulateReload(participant: DemoParticipant): DemoParticipant {
  const payload = simulateDbPayload(participant);
  const { participant: repaired } = repairScalarCompensationProfile({
    ...payload,
    id: payload.id ?? 'row-id',
    dealId: payload.dealId ?? 'deal-id',
    inviteToken: payload.inviteToken ?? 'token',
  });
  return repaired;
}

function field<T>(value: T, confidence: 'high' | 'medium' | 'low' | 'absent' = 'high') {
  return { value, confidence };
}

